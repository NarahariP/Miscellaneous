
public class CheckBox {

	private Integer id;
	
	private String code;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public CheckBox(Integer id, String code) {
		super();
		this.id = id;
		this.code = code;
	}
	
}
